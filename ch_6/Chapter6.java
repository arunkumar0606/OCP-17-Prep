/*
CLASS Modifiers
1) public - accessible from anywhere
2) default (no modifier) - accessible only within the same package
3) final - cannot be subclassed
4) abstract - cannot be instantiated, can have abstract methods that must be implemented by subclasses
5) static - nested classes can be static, but top-level classes cannot be static
 */
class Interview{
    String companyName;
    static int age=9;
    protected int noticePeriod =15;
}

class Tcs extends Interview{
    int noticePeriod=90;
    String location="Chennai";
    public void setCompanyName(String companyName) {
        this.companyName = companyName;
        age=1;
        System.out.println(super.noticePeriod); //15
        System.out.println(noticePeriod); //90  current class variable
        System.out.println(this.noticePeriod); //90 current class variable
        System.out.println(this.location);
       // System.out.println(super.location); // doest compile
    }
    public static void main(String[] args) {
        Tcs tcs = new Tcs();
        tcs.setCompanyName("TCS");
        System.out.println("Company Name: " + tcs.companyName);
        System.out.println("Age: " + Tcs.age); //1
        System.out.println("Age: " + Interview.age); //1
        System.out.println("TCS Notice Period: " + tcs.noticePeriod);
    }
}


/*
CONSTRUCTOR
Name should match class name
no return type
var is not allowed in constructor parameters

 */
class ConstructorExample{
    String name;
    int age;

    // Default constructor

    private ConstructorExample() {
        this.name = "Default Name";
        this.age = 0;
    }
    //var not allowed in constructor parameters
//    public ConstructorExample(var age){
//        this.name="Default Name";
//        this.age=age;
//    }

    // Parameterized constructor
    public ConstructorExample(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public static void main(String[] args) {
        ConstructorExample example1 = new ConstructorExample();
        System.out.println("Example 1 - Name: " + example1.name + ", Age: " + example1.age);

        ConstructorExample example2 = new ConstructorExample("Alice", 30);
        System.out.println("Example 2 - Name: " + example2.name + ", Age: " + example2.age);
    }
}
