//this () - calling another constructor in the same class
//this() must be the first statement in the constructor
class Sample2{
    String name;
    int age;
    public Sample2(int age){
        this.age=age;
        this.name="Default Name";
    }
    public Sample2(String name, int age){
       // System.out.println("first line"); -- adding this line will cause compile error because this() must be the first statement
        this(age); // calling the constructor with int parameter
       // Sample2(age); // not possible
        this.name=name;
    }
    public static void main(String[] args) {
        Sample2 sample1 = new Sample2(25);
        System.out.println("Name: " + sample1.name + ", Age: " + sample1.age);
        Sample2 sample = new Sample2("Alice", 30);
        System.out.println("Name: " + sample.name + ", Age: " + sample.age);

    }
}

//super() - calling parent class constructor
// super() must be the first statement in the constructor
class Parent{
    String name;
    public Parent(String name){
        this.name=name;
    }
}
class Child extends Parent{
    int age;
    public Child(String name){
        //System.out.println("hi"); //doesnt compile
        super(name); // calling parent constructor
        this.age=12;
    }
    public Child(){
        this("arun");
    }
    public static void main(String[] args) {
        Child child = new Child();
        System.out.println("Name: " + child.name + ", Age: " + child.age);
    }
}

class Animal {
    public  int age;
    public String name;
    public Animal(int age, String name) {
        super(); //repeated call is allowed
        this.age = age;
        this.name = name;
    }
    public Animal(int age) {
        super();
        this.age = age;
        this.name = null;
    }
}
class Gorilla extends Animal {
    public Gorilla(int age) {
        super(age,"Gorilla"); // Calls the first Animal constructor
    }
    public Gorilla() {
        super(5); // Calls the second Animal constructor
    }
}
class GorillaTest {
    public static void main(String[] args) {
        Gorilla gorilla1 = new Gorilla(10);
        Gorilla gorilla2 = new Gorilla();
        System.out.println("Gorilla 1 - Age: " + gorilla1.age + ", Name: " + gorilla1.name); // Age: 10, Name: Gorilla
        System.out.println("Gorilla 2 - Age: " + gorilla2.age + ", Name: " + gorilla2.name); // Age: 5, Name: null
    }
}

